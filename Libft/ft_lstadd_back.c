#include "libft.h"

void ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*backup;

	backup = ft_lstlast(*lst);
	if(!backup){
		*lst = new;
	}
	else {
		backup->next = new;
	}
}
