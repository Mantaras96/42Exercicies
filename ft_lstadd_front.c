#include "libft.h"

void	ft_lstadd_front(t_list **lst, t_list *new)
{
	t_list	*new_list;

	if (*lst == NULL)
		new_list = new;
	new_list = new;
	new_list->next = *lst;
	*lst = new_list;
}
